"""
Count the number of times digit 0 appears in all numbers from 1 to N (inclusive).
"""

def count_zeros(n):
    count = 0
    factor = 1

    while factor <= n:
        lower = n % factor
        curr = (n // factor) % 10
        higher = n // (factor * 10)

        if curr == 0:
            count += higher * factor + lower + 1
        else:
            count += higher * factor

        factor *= 10

    return count

if __name__ == "__main__":
    print(count_zeros(105))  # Example usage